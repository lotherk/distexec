int
cmdline_parser_required (struct gengetopt_args_info *args_info, const char *prog_name)
{
  int result = EXIT_SUCCESS;

  if (cmdline_parser_required2(args_info, prog_name, 0) > 0)
    result = EXIT_FAILURE;

  if (result == EXIT_FAILURE)
    {
      cmdline_parser_free (args_info);
      exit (EXIT_FAILURE);
    }
  
  return result;
}

int
cmdline_parser_required2 (struct gengetopt_args_info *args_info, const char *prog_name, const char *additional_error)
{
  int error_occurred = 0;
  FIX_UNUSED (additional_error);

  /* checks for required options */
  if (check_multiple_option_occurrences(prog_name, args_info->config_given, args_info->config_min, args_info->config_max, "'--config' ('-C')"))
     error_occurred = 1;
  
  if (check_multiple_option_occurrences(prog_name, args_info->plugin_path_given, args_info->plugin_path_min, args_info->plugin_path_max, "'--plugin-path'"))
     error_occurred = 1;
  
  if (check_multiple_option_occurrences(prog_name, args_info->plugin_given, args_info->plugin_min, args_info->plugin_max, "'--plugin' ('-p')"))
     error_occurred = 1;
  
  if (check_multiple_option_occurrences(prog_name, args_info->grep_given, args_info->grep_min, args_info->grep_max, "'--grep' ('-g')"))
     error_occurred = 1;
  
  if (check_multiple_option_occurrences(prog_name, args_info->env_given, args_info->env_min, args_info->env_max, "'--env' ('-E')"))
     error_occurred = 1;
  
  
  /* checks for dependences among options */

  return error_occurred;
}


static char *package_name = 0;

/**
 * @brief updates an option
 * @param field the generic pointer to the field to update
 * @param orig_field the pointer to the orig field
 * @param field_given the pointer to the number of occurrence of this option
 * @param prev_given the pointer to the number of occurrence already seen
 * @param value the argument for this option (if null no arg was specified)
 * @param possible_values the possible values for this option (if specified)
 * @param default_value the default value (in case the option only accepts fixed values)
 * @param arg_type the type of this option
 * @param check_ambiguity @see cmdline_parser_params.check_ambiguity
 * @param override @see cmdline_parser_params.override
 * @param no_free whether to free a possible previous value
 * @param multiple_option whether this is a multiple option
 * @param long_opt the corresponding long option
 * @param short_opt the corresponding short option (or '-' if none)
 * @param additional_error possible further error specification
 */
static
int update_arg(void *field, char **orig_field,
               unsigned int *field_given, unsigned int *prev_given, 
               char *value, const char *possible_values[],
               const char *default_value,
               cmdline_parser_arg_type arg_type,
               int check_ambiguity, int override,
               int no_free, int multiple_option,
               const char *long_opt, char short_opt,
               const char *additional_error)
{
  char *stop_char = 0;
  const char *val = value;
  int found;
  char **string_field;
  FIX_UNUSED (field);

  stop_char = 0;
  found = 0;

  if (!multiple_option && prev_given && (*prev_given || (check_ambiguity && *field_given)))
    {
      if (short_opt != '-')
        fprintf (stderr, "%s: `--%s' (`-%c') option given more than once%s\n", 
               package_name, long_opt, short_opt,
               (additional_error ? additional_error : ""));
      else
        fprintf (stderr, "%s: `--%s' option given more than once%s\n", 
               package_name, long_opt,
               (additional_error ? additional_error : ""));
      return 1; /* failure */
    }

  FIX_UNUSED (default_value);
    
  if (field_given && *field_given && ! override)
    return 0;
  if (prev_given)
    (*prev_given)++;
  if (field_given)
    (*field_given)++;
  if (possible_values)
    val = possible_values[found];

  switch(arg_type) {
  case ARG_FLAG:
    *((int *)field) = !*((int *)field);
    break;
  case ARG_INT:
    if (val) *((int *)field) = strtol (val, &stop_char, 0);
    break;
  case ARG_STRING:
    if (val) {
      string_field = (char **)field;
      if (!no_free && *string_field)
        free (*string_field); /* free previous string */
      *string_field = gengetopt_strdup (val);
    }
    break;
  default:
    break;
  };

  /* check numeric conversion */
  switch(arg_type) {
  case ARG_INT:
    if (val && !(stop_char && *stop_char == '\0')) {
      fprintf(stderr, "%s: invalid numeric value: %s\n", package_name, val);
      return 1; /* failure */
    }
    break;
  default:
    ;
  };

  /* store the original value */
  switch(arg_type) {
  case ARG_NO:
  case ARG_FLAG:
    break;
  default:
    if (value && orig_field) {
      if (no_free) {
        *orig_field = value;
      } else {
        if (*orig_field)
          free (*orig_field); /* free previous string */
        *orig_field = gengetopt_strdup (value);
      }
    }
  };

  return 0; /* OK */
}

/**
 * @brief store information about a multiple option in a temporary list
 * @param list where to (temporarily) store multiple options
 */
static
int update_multiple_arg_temp(struct generic_list **list,
               unsigned int *prev_given, const char *val,
               const char *possible_values[], const char *default_value,
               cmdline_parser_arg_type arg_type,
               const char *long_opt, char short_opt,
               const char *additional_error)
{
  /* store single arguments */
  char *multi_token;
  const char *multi_next;

  if (arg_type == ARG_NO) {
    (*prev_given)++;
    return 0; /* OK */
  }

  multi_token = get_multiple_arg_token(val);
  multi_next = get_multiple_arg_token_next (val);

  while (1)
    {
      add_node (list);
      if (update_arg((void *)&((*list)->arg), &((*list)->orig), 0,
          prev_given, multi_token, possible_values, default_value, 
          arg_type, 0, 1, 1, 1, long_opt, short_opt, additional_error)) {
        if (multi_token) free(multi_token);
        return 1; /* failure */
      }

      if (multi_next)
        {
          multi_token = get_multiple_arg_token(multi_next);
          multi_next = get_multiple_arg_token_next (multi_next);
        }
      else
        break;
    }

  return 0; /* OK */
}

/**
 * @brief free the passed list (including possible string argument)
 */
static
void free_list(struct generic_list *list, short string_arg)
{
  if (list) {
    struct generic_list *tmp;
    while (list)
      {
        tmp = list;
        if (string_arg && list->arg.string_arg)
          free (list->arg.string_arg);
        if (list->orig)
          free (list->orig);
        list = list->next;
        free (tmp);
      }
  }
}

/**
 * @brief updates a multiple option starting from the passed list
 */
static
void update_multiple_arg(void *field, char ***orig_field,
               unsigned int field_given, unsigned int prev_given, union generic_value *default_value,
               cmdline_parser_arg_type arg_type,
               struct generic_list *list)
{
  int i;
  struct generic_list *tmp;

  if (prev_given && list) {
    *orig_field = (char **) realloc (*orig_field, (field_given + prev_given) * sizeof (char *));

    switch(arg_type) {
    case ARG_INT:
      *((int **)field) = (int *)realloc (*((int **)field), (field_given + prev_given) * sizeof (int)); break;
    case ARG_STRING:
      *((char ***)field) = (char **)realloc (*((char ***)field), (field_given + prev_given) * sizeof (char *)); break;
    default:
      break;
    };
    
    for (i = (prev_given - 1); i >= 0; --i)
      {
        tmp = list;
        
        switch(arg_type) {
        case ARG_INT:
          (*((int **)field))[i + field_given] = tmp->arg.int_arg; break;
        case ARG_STRING:
          (*((char ***)field))[i + field_given] = tmp->arg.string_arg; break;
        default:
          break;
        }        
        (*orig_field) [i + field_given] = list->orig;
        list = list->next;
        free (tmp);
      }
  } else { /* set the default value */
    if (default_value && ! field_given) {
      switch(arg_type) {
      case ARG_INT:
        if (! *((int **)field)) {
          *((int **)field) = (int *)malloc (sizeof (int));
          (*((int **)field))[0] = default_value->int_arg; 
        }
        break;
      case ARG_STRING:
        if (! *((char ***)field)) {
          *((char ***)field) = (char **)malloc (sizeof (char *));
          (*((char ***)field))[0] = gengetopt_strdup(default_value->string_arg);
        }
        break;
      default: break;
      }
      if (!(*orig_field)) {
        *orig_field = (char **) malloc (sizeof (char *));
        (*orig_field)[0] = 0;
      }
    }
  }
}

int
cmdline_parser_internal (
  int argc, char **argv, struct gengetopt_args_info *args_info,
                        struct cmdline_parser_params *params, const char *additional_error)
{
  int c;	/* Character of the parsed option.  */
  union generic_value multiple_default_value;

  struct generic_list * config_list = NULL;
  struct generic_list * plugin_path_list = NULL;
  struct generic_list * plugin_list = NULL;
  struct generic_list * grep_list = NULL;
  struct generic_list * env_list = NULL;
  int error_occurred = 0;
  struct gengetopt_args_info local_args_info;
  
  int override;
  int initialize;
  int check_required;
  int check_ambiguity;
  
  package_name = argv[0];
  
  override = params->override;
  initialize = params->initialize;
  check_required = params->check_required;
  check_ambiguity = params->check_ambiguity;

  if (initialize)
    cmdline_parser_init (args_info);

  cmdline_parser_init (&local_args_info);

  optarg = 0;
  optind = 0;
  opterr = params->print_errors;
  optopt = '?';

  while (1)
    {
      int option_index = 0;

      static struct option long_options[] = {
        { "help",	0, NULL, 'h' },
        { "detailed-help",	0, NULL, 0 },
        { "full-help",	0, NULL, 0 },
        { "version",	0, NULL, 'V' },
        { "config",	1, NULL, 'C' },
        { "output",	0, NULL, 'O' },
        { "stdout",	0, NULL, 0 },
        { "stderr",	0, NULL, 0 },
        { "progress",	0, NULL, 'P' },
        { "list",	0, NULL, 'l' },
        { "plugin-path",	1, NULL, 0 },
        { "plugin",	1, NULL, 'p' },
        { "filter",	1, NULL, 'F' },
        { "grep",	1, NULL, 'g' },
        { "env",	1, NULL, 'E' },
        { "concurrent",	1, NULL, 'c' },
        { "log-split",	0, NULL, 'S' },
        { "log-file",	1, NULL, 'L' },
        { "log-format-date",	1, NULL, 0 },
        { "log-format-time",	1, NULL, 0 },
        { "log-format",	1, NULL, 0 },
        { "debug",	0, NULL, 0 },
        { 0,  0, 0, 0 }
      };

      c = getopt_long (argc, argv, "hVC:OPlp:F:g:E:c:SL:", long_options, &option_index);

      if (c == -1) break;	/* Exit from `while (1)' loop.  */

      switch (c)
        {
        case 'h':	/* Print help and exit.  */
          cmdline_parser_print_help ();
          cmdline_parser_free (&local_args_info);
          exit (EXIT_SUCCESS);

        case 'V':	/* Print version and exit.  */
          cmdline_parser_print_version ();
          cmdline_parser_free (&local_args_info);
          exit (EXIT_SUCCESS);

        case 'C':	/* Configuration file.  */
        
          if (update_multiple_arg_temp(&config_list, 
              &(local_args_info.config_given), optarg, 0, 0, ARG_STRING,
              "config", 'C',
              additional_error))
            goto failure;
        
          break;
        case 'O':	/* Use --stdout and --stdin combined.  */
        
        
          if (update_arg( 0 , 
               0 , &(args_info->output_given),
              &(local_args_info.output_given), optarg, 0, 0, ARG_NO,
              check_ambiguity, override, 0, 0,
              "output", 'O',
              additional_error))
            goto failure;
        
          break;
        case 'P':	/* Print progressbar.  */
        
        
          if (update_arg( 0 , 
               0 , &(args_info->progress_given),
              &(local_args_info.progress_given), optarg, 0, 0, ARG_NO,
              check_ambiguity, override, 0, 0,
              "progress", 'P',
              additional_error))
            goto failure;
        
          break;
        case 'l':	/* Print hosts.  */
        
        
          if (update_arg( 0 , 
               0 , &(args_info->list_given),
              &(local_args_info.list_given), optarg, 0, 0, ARG_NO,
              check_ambiguity, override, 0, 0,
              "list", 'l',
              additional_error))
            goto failure;
        
          break;
        case 'p':	/* Plugin to load.  */
        
          if (update_multiple_arg_temp(&plugin_list, 
              &(local_args_info.plugin_given), optarg, 0, 0, ARG_STRING,
              "plugin", 'p',
              additional_error))
            goto failure;
        
          break;
        case 'F':	/* Filter query.  */
        
        
          if (update_arg( (void *)&(args_info->filter_arg), 
               &(args_info->filter_orig), &(args_info->filter_given),
              &(local_args_info.filter_given), optarg, 0, 0, ARG_STRING,
              check_ambiguity, override, 0, 0,
              "filter", 'F',
              additional_error))
            goto failure;
        
          break;
        case 'g':	/* Filter output .  */
        
          if (update_multiple_arg_temp(&grep_list, 
              &(local_args_info.grep_given), optarg, 0, 0, ARG_STRING,
              "grep", 'g',
              additional_error))
            goto failure;
        
          break;
        case 'E':	/* Environment variable.  */
        
          if (update_multiple_arg_temp(&env_list, 
              &(local_args_info.env_given), optarg, 0, 0, ARG_STRING,
              "env", 'E',
              additional_error))
            goto failure;
        
          break;
        case 'c':	/* Number of concurrent connection threads.  */
        
        
          if (update_arg( (void *)&(args_info->concurrent_arg), 
               &(args_info->concurrent_orig), &(args_info->concurrent_given),
              &(local_args_info.concurrent_given), optarg, 0, "1", ARG_INT,
              check_ambiguity, override, 0, 0,
              "concurrent", 'c',
              additional_error))
            goto failure;
        
          break;
        case 'S':	/* Split logfile per host (needs --log-file).  */
        
        
          if (update_arg( 0 , 
               0 , &(args_info->log_split_given),
              &(local_args_info.log_split_given), optarg, 0, 0, ARG_NO,
              check_ambiguity, override, 0, 0,
              "log-split", 'S',
              additional_error))
            goto failure;
        
          break;
        case 'L':	/* Log to file instead of stdout.  */
        
        
          if (update_arg( (void *)&(args_info->log_file_arg), 
               &(args_info->log_file_orig), &(args_info->log_file_given),
              &(local_args_info.log_file_given), optarg, 0, 0, ARG_STRING,
              check_ambiguity, override, 0, 0,
              "log-file", 'L',
              additional_error))
            goto failure;
        
          break;

        case 0:	/* Long option with no short option */
          if (strcmp (long_options[option_index].name, "detailed-help") == 0) {
            cmdline_parser_print_detailed_help ();
            cmdline_parser_free (&local_args_info);
            exit (EXIT_SUCCESS);
          }

          if (strcmp (long_options[option_index].name, "full-help") == 0) {
            cmdline_parser_print_full_help ();
            cmdline_parser_free (&local_args_info);
            exit (EXIT_SUCCESS);
          }

          /* Print remote stdout.  */
          if (strcmp (long_options[option_index].name, "stdout") == 0)
          {
          
          
            if (update_arg( 0 , 
                 0 , &(args_info->stdout_given),
                &(local_args_info.stdout_given), optarg, 0, 0, ARG_NO,
                check_ambiguity, override, 0, 0,
                "stdout", '-',
                additional_error))
              goto failure;
          
          }
          /* Print remote stderr.  */
          else if (strcmp (long_options[option_index].name, "stderr") == 0)
          {
          
          
            if (update_arg( 0 , 
                 0 , &(args_info->stderr_given),
                &(local_args_info.stderr_given), optarg, 0, 0, ARG_NO,
                check_ambiguity, override, 0, 0,
                "stderr", '-',
                additional_error))
              goto failure;
          
          }
          /* Path to plugins.  */
          else if (strcmp (long_options[option_index].name, "plugin-path") == 0)
          {
          
            if (update_multiple_arg_temp(&plugin_path_list, 
                &(local_args_info.plugin_path_given), optarg, 0, "/usr/local/share/distexec/plugins", ARG_STRING,
                "plugin-path", '-',
                additional_error))
              goto failure;
          
          }
          /* Date format.  */
          else if (strcmp (long_options[option_index].name, "log-format-date") == 0)
          {
          
          
            if (update_arg( (void *)&(args_info->log_format_date_arg), 
                 &(args_info->log_format_date_orig), &(args_info->log_format_date_given),
                &(local_args_info.log_format_date_given), optarg, 0, "%Y-%m-%d", ARG_STRING,
                check_ambiguity, override, 0, 0,
                "log-format-date", '-',
                additional_error))
              goto failure;
          
          }
          /* Time format.  */
          else if (strcmp (long_options[option_index].name, "log-format-time") == 0)
          {
          
          
            if (update_arg( (void *)&(args_info->log_format_time_arg), 
                 &(args_info->log_format_time_orig), &(args_info->log_format_time_given),
                &(local_args_info.log_format_time_given), optarg, 0, "%H:%M:%S", ARG_STRING,
                check_ambiguity, override, 0, 0,
                "log-format-time", '-',
                additional_error))
              goto failure;
          
          }
          /* Log format.  */
          else if (strcmp (long_options[option_index].name, "log-format") == 0)
          {
          
          
            if (update_arg( (void *)&(args_info->log_format_arg), 
                 &(args_info->log_format_orig), &(args_info->log_format_given),
                &(local_args_info.log_format_given), optarg, 0, 0, ARG_STRING,
                check_ambiguity, override, 0, 0,
                "log-format", '-',
                additional_error))
              goto failure;
          
          }
          /* Enable debug messages.  */
          else if (strcmp (long_options[option_index].name, "debug") == 0)
          {
          
          
            if (update_arg((void *)&(args_info->debug_flag), 0, &(args_info->debug_given),
                &(local_args_info.debug_given), optarg, 0, 0, ARG_FLAG,
                check_ambiguity, override, 1, 0, "debug", '-',
                additional_error))
              goto failure;
          
          }
          
          break;
        case '?':	/* Invalid option.  */
          /* `getopt_long' already printed an error message.  */
          goto failure;

        default:	/* bug: option not considered.  */
          fprintf (stderr, "%s: option unknown: %c%s\n", CMDLINE_PARSER_PACKAGE, c, (additional_error ? additional_error : ""));
          abort ();
        } /* switch */
    } /* while */


  update_multiple_arg((void *)&(args_info->config_arg),
    &(args_info->config_orig), args_info->config_given,
    local_args_info.config_given, 0,
    ARG_STRING, config_list);
  multiple_default_value.default_string_arg = "/usr/local/share/distexec/plugins";
  update_multiple_arg((void *)&(args_info->plugin_path_arg),
    &(args_info->plugin_path_orig), args_info->plugin_path_given,
    local_args_info.plugin_path_given, &multiple_default_value,
    ARG_STRING, plugin_path_list);
  update_multiple_arg((void *)&(args_info->plugin_arg),
    &(args_info->plugin_orig), args_info->plugin_given,
    local_args_info.plugin_given, 0,
    ARG_STRING, plugin_list);
  update_multiple_arg((void *)&(args_info->grep_arg),
    &(args_info->grep_orig), args_info->grep_given,
    local_args_info.grep_given, 0,
    ARG_STRING, grep_list);
  update_multiple_arg((void *)&(args_info->env_arg),
    &(args_info->env_orig), args_info->env_given,
    local_args_info.env_given, 0,
    ARG_STRING, env_list);

  args_info->config_given += local_args_info.config_given;
  local_args_info.config_given = 0;
  args_info->plugin_path_given += local_args_info.plugin_path_given;
  local_args_info.plugin_path_given = 0;
  args_info->plugin_given += local_args_info.plugin_given;
  local_args_info.plugin_given = 0;
  args_info->grep_given += local_args_info.grep_given;
  local_args_info.grep_given = 0;
  args_info->env_given += local_args_info.env_given;
  local_args_info.env_given = 0;
  
  if (check_required)
    {
      error_occurred += cmdline_parser_required2 (args_info, argv[0], additional_error);
    }

  cmdline_parser_release (&local_args_info);

  if ( error_occurred )
    return (EXIT_FAILURE);

  if (optind < argc)
    {
      int i = 0 ;
      int found_prog_name = 0;
      /* whether program name, i.e., argv[0], is in the remaining args
         (this may happen with some implementations of getopt,
          but surely not with the one included by gengetopt) */

      i = optind;
      while (i < argc)
        if (argv[i++] == argv[0]) {
          found_prog_name = 1;
          break;
        }
      i = 0;

      args_info->inputs_num = argc - optind - found_prog_name;
      args_info->inputs =
        (char **)(malloc ((args_info->inputs_num)*sizeof(char *))) ;
      while (optind < argc)
        if (argv[optind++] != argv[0])
          args_info->inputs[ i++ ] = gengetopt_strdup (argv[optind-1]) ;
    }

  return 0;

failure:
  free_list (config_list, 1 );
  free_list (plugin_path_list, 1 );
  free_list (plugin_list, 1 );
  free_list (grep_list, 1 );
  free_list (env_list, 1 );
  
  cmdline_parser_release (&local_args_info);
  return (EXIT_FAILURE);
}

#ifndef CONFIG_FILE_LINE_SIZE
#define CONFIG_FILE_LINE_SIZE 2048
#endif
#define ADDITIONAL_ERROR " in configuration file "

#define CONFIG_FILE_LINE_BUFFER_SIZE (CONFIG_FILE_LINE_SIZE+3)
/* 3 is for "--" and "=" */

static int
_cmdline_parser_configfile (const char *filename, int *my_argc)
{
  FILE* file;
  char my_argv[CONFIG_FILE_LINE_BUFFER_SIZE+1];
  char linebuf[CONFIG_FILE_LINE_SIZE];
  int line_num = 0;
  int result = 0, equal;
  char *fopt, *farg;
  char *str_index;
  size_t len, next_token;
  char delimiter;

  if ((file = fopen(filename, "r")) == 0)
    {
      fprintf (stderr, "%s: Error opening configuration file '%s'\n",
               CMDLINE_PARSER_PACKAGE, filename);
      return EXIT_FAILURE;
    }

  while ((fgets(linebuf, CONFIG_FILE_LINE_SIZE, file)) != 0)
    {
      ++line_num;
      my_argv[0] = '\0';
      len = strlen(linebuf);
      if (len > (CONFIG_FILE_LINE_BUFFER_SIZE-1))
        {
          fprintf (stderr, "%s:%s:%d: Line too long in configuration file\n",
                   CMDLINE_PARSER_PACKAGE, filename, line_num);
          result = EXIT_FAILURE;
          break;
        }

      /* find first non-whitespace character in the line */
      next_token = strspn (linebuf, " \t\r\n");
      str_index  = linebuf + next_token;

      if ( str_index[0] == '\0' || str_index[0] == '#')
        continue; /* empty line or comment line is skipped */

      fopt = str_index;

      /* truncate fopt at the end of the first non-valid character */
      next_token = strcspn (fopt, " \t\r\n=");

      if (fopt[next_token] == '\0') /* the line is over */
        {
          farg  = 0;
          equal = 0;
          goto noarg;
        }

      /* remember if equal sign is present */
      equal = (fopt[next_token] == '=');
      fopt[next_token++] = '\0';

      /* advance pointers to the next token after the end of fopt */
      next_token += strspn (fopt + next_token, " \t\r\n");

      /* check for the presence of equal sign, and if so, skip it */
      if ( !equal )
        if ((equal = (fopt[next_token] == '=')))
          {
            next_token++;
            next_token += strspn (fopt + next_token, " \t\r\n");
          }
      str_index  += next_token;

      /* find argument */
      farg = str_index;
      if ( farg[0] == '\"' || farg[0] == '\'' )
        { /* quoted argument */
          str_index = strchr (++farg, str_index[0] ); /* skip opening quote */
          if (! str_index)
            {
              fprintf
                (stderr,
                 "%s:%s:%d: unterminated string in configuration file\n",
                 CMDLINE_PARSER_PACKAGE, filename, line_num);
              result = EXIT_FAILURE;
              break;
            }
        }
      else
        { /* read up the remaining part up to a delimiter */
          next_token = strcspn (farg, " \t\r\n#\'\"");
          str_index += next_token;
        }

      /* truncate farg at the delimiter and store it for further check */
      delimiter = *str_index, *str_index++ = '\0';

      /* everything but comment is illegal at the end of line */
      if (delimiter != '\0' && delimiter != '#')
        {
          str_index += strspn(str_index, " \t\r\n");
          if (*str_index != '\0' && *str_index != '#')
            {
              fprintf
                (stderr,
                 "%s:%s:%d: malformed string in configuration file\n",
                 CMDLINE_PARSER_PACKAGE, filename, line_num);
              result = EXIT_FAILURE;
              break;
            }
        }

    noarg:
      if (!strcmp(fopt,"include")) {
        if (farg && *farg) {
          result = _cmdline_parser_configfile(farg, my_argc);
        } else {
          fprintf(stderr, "%s:%s:%d: include requires a filename argument.\n",
                  CMDLINE_PARSER_PACKAGE, filename, line_num);
        }
        continue;
      }
      len = strlen(fopt);
      strcat (my_argv, len > 1 ? "--" : "-");
      strcat (my_argv, fopt);
      if (len > 1 && ((farg && *farg) || equal))
        strcat (my_argv, "=");
      if (farg && *farg)
        strcat (my_argv, farg);
      ++(*my_argc);

      cmd_line_list_tmp = (struct line_list *) malloc (sizeof (struct line_list));
      cmd_line_list_tmp->next = cmd_line_list;
      cmd_line_list = cmd_line_list_tmp;
      cmd_line_list->string_arg = gengetopt_strdup(my_argv);
    } /* while */

  if (file)
    fclose(file);
  return result;
}

int
cmdline_parser_configfile (
  const char *filename,
                           struct gengetopt_args_info *args_info,
                           int override, int initialize, int check_required)
{
  struct cmdline_parser_params params;

  params.override = override;
  params.initialize = initialize;
  params.check_required = check_required;
  params.check_ambiguity = 0;
  params.print_errors = 1;
  
  return cmdline_parser_config_file (filename, args_info, &params);
}

int
cmdline_parser_config_file (const char *filename,
                           struct gengetopt_args_info *args_info,
                           struct cmdline_parser_params *params)
{
  int i, result;
  int my_argc = 1;
  char **my_argv_arg;
  char *additional_error;

  /* store the program name */
  cmd_line_list_tmp = (struct line_list *) malloc (sizeof (struct line_list));
  cmd_line_list_tmp->next = cmd_line_list;
  cmd_line_list = cmd_line_list_tmp;
  cmd_line_list->string_arg = gengetopt_strdup (CMDLINE_PARSER_PACKAGE);

  result = _cmdline_parser_configfile(filename, &my_argc);

  if (result != EXIT_FAILURE) {
    my_argv_arg = (char **) malloc((my_argc+1) * sizeof(char *));
    cmd_line_list_tmp = cmd_line_list;

    for (i = my_argc - 1; i >= 0; --i) {
      my_argv_arg[i] = cmd_line_list_tmp->string_arg;
      cmd_line_list_tmp = cmd_line_list_tmp->next;
    }

    my_argv_arg[my_argc] = 0;

    additional_error = (char *)malloc(strlen(filename) + strlen(ADDITIONAL_ERROR) + 1);
    strcpy (additional_error, ADDITIONAL_ERROR);
    strcat (additional_error, filename);
    result =
      cmdline_parser_internal (my_argc, my_argv_arg, args_info,
                              params,
                              additional_error);

    free (additional_error);
    free (my_argv_arg);
  }

  free_cmd_list();
  if (result == EXIT_FAILURE)
    {
      cmdline_parser_free (args_info);
      exit (EXIT_FAILURE);
    }
  
  return result;
}
